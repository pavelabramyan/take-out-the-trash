def page_estimate(text: str) -> float:
    return len(text) / CHARS_PER_PAGE


def h(level: int, title: str) -> str:
    return "#" * level + f" {title}\n\n"


def p(*paras: str) -> str:
    return "\n\n".join(paras) + "\n\n"


def req(rid: str, title: str, now: str, target: str, ac: list[str], prio: str, deps: str, test: str) -> str:
    lines = [
        f"### {rid}. {title}",
        "",
        f"**Приоритет:** {prio}  ",
        f"**Зависимости:** {deps}  ",
        f"**Файл/зона:** см. контекст раздела  ",
        "",
        f"**Как сейчас:** {now}",
        "",
        f"**Целевое состояние:** {target}",
        "",
        "**Критерии приёмки:**",
    ]
    for i, a in enumerate(ac, 1):
        lines.append(f"{i}. {a}")
    lines += ["", f"**Проверка:** {test}", "", "---", ""]
    return "\n".join(lines)


